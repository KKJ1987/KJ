package 조건문기초;

import java.util.Scanner;

public class ex05ifelse문 {

	public static void main(String[] args) {

		int age = 21;
		
		// age가 20보다 크거나 같다면 성인입니다 를 출력
		if(age >= 20) {
			// 조건식이 참이었을 때 실행하는 구간
			System.out.println("성인입니다.");
		}else {
			// 그렇지 않은 경우 성인이 아닙니다를 출력
			// 코드 이동 단축키는 alt 누르고 방향키 위 아래로
			// 조건식이 거짓이었을 때 실행하는 구간
			System.out.println("성인이 아닙니다.");
		}
		
		
		
		
		
		
		
		
		
		
		
	}

}
